#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	if (!s || s[0] == 0)
		return (0);
	i = ft_strlen(s);
	while (i > 0)
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i--;
	}
	if (s[i] == (char)c)
		return ((char *)s + i);
	return (0);
}
